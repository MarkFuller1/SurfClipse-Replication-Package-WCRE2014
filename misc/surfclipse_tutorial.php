<h3 id="proactive">Working in Pro-active Mode</h3>
<div>
In this mode, the plug-in is responsible to select the search query and initiate the search. Basically, the plug-in continuously checks if there is an exception occurred in the <i>Console View</i>. Once an exception occurred, it automatically extracts necessary information for search query, and initiates the search.
</div>
