<h3>Using SurfClipse</h3>
<p>SurfClipse facilitates Eclipse users to conduct web search from within the IDE. More interestingly, the plug-in can capture the context of the search to provide more targeted and useful results specially during exception handling. It works in two modes: interactive mode and pro-active mode. 
</p>
<h3 id="interactive">Working in Interactive Mode</h3>
<div>In this mode, <b>you will be responsible to select the search query and initiate the search operation</b>. You can select a phrase (e.g., any error or exception message) from <i>Console View</i> or <i>Error Log</i>) as the query and start search from context-menu option. At first, you have to choose the Interactive mode.</div>
