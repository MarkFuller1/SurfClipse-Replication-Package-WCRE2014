<h2>Browsing History Survey</h2>
<strong>Objectives of survey:</strong>To discover the web usage patterns of the researchers and developers for bug fixation and error handling.
<p>
<strong>Project:</strong> SurfClipse
</p>
<p>
<strong>Survey Overview</strong>:
<br />
Error or exception handling, bug fixation are very common activities for the software developers and researchers. Study shows that they spend at least 19% of their work hour in browsing web and searching for information. Recently, we are working on SurfClipse, an IDE based web search tool that can capture an occurred exception or runtime error, collects results from different search engines and display the results within the context of IDE in a proactive fashion. However, the tool will benefit if we can determine the usage of web information for bug fixation or error handling. So, we have decided to conduct the survey on the web browsing history of the intended participants. We need browsing history data for the last <b>6 months</b></p>
<p>
